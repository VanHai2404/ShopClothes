 private void insert() {
        KhachHang kh = getForm();
        String MaKH = txtMaKH.getText();
        KhachHang khachhang = daokh.selectById(MaKH);
        if (khachhang != null) {
            DiaLogHelper.alert(this, "Mã KH đã tồn Tại !! ");

        } else {
            try {
                daokh.insert(kh);
                DiaLogHelper.alert(this, "Thêm Mới Khách Hàng Thành  Công");
                fillTable();
            } catch (Exception e) {
                DiaLogHelper.alert(this, e.getMessage());
            }

        }
    }

    private void update() {
        KhachHang kh = getForm();
        String MaKH = txtMaKH.getText();
        KhachHang khachhang = daokh.selectById(MaKH);
          if (DiaLogHelper.confirm(this, "Bạn có muốn cập nhật thông tin khách hàng này")) {
        if (khachhang != null) {
            try {
                daokh.update(kh);
                fillTable();
                DiaLogHelper.alert(this, "Cập nhât thông tin khách hàng thành công ! ");
            } catch (Exception e) {
                DiaLogHelper.showErrorDialog(this, e.getMessage(), "Lỗi Truy Vẫn Dữ Liệu !!");
            }

        } else {
            DiaLogHelper.alert(this, "Mã KH Ko Tồn Tại");
        }
          }

    }

    private void delete() {
        String MaKH = txtMaKH.getText();
        KhachHang khachhang = daokh.selectById(MaKH);
          if (DiaLogHelper.confirm(this, "Bạn có muốn xoá khách hàng này")) {
        try {
            if (khachhang != null) {
                daokh.delete(MaKH);
                fillTable();
                DiaLogHelper.alert(this, "Xóa KH thành công !! ");

            } else {
                DiaLogHelper.alert(this, "Mã KH ko tồn tại !! ");
            }
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Lỗi truy vẫn dữ liệu !! ");
        }

    }
    }

