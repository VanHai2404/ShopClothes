    void check() {

        if ((txtMaKH.getText()).length() < 5 || (txtMaKH.getText()).length() > 5) {
            if ((txtMaKH.getText()).equals("")) {
                DiaLogHelper.alert(this, "Mã người học không được để trống");

            } else {
                DiaLogHelper.alert(this, "Mã người học phải nhập đúng 5 ký tự");
            }
            
        } else if ((txtTenKH.getText()).equals("")) {
            DiaLogHelper.alert(this, "Họ tên không được để trống");
//        } else if (!txtTenKH.getText().matches("[a-zA-Z][a-zA-Z ]+")) {
//            DiaLogHelper.alert(this, "Họ tên chỉ chứa alphabet và ký tự trắng");
            
        } else if (txtSoDT.getText().equals("")) {
            DiaLogHelper.alert(this, "Số điện thoại không được để trống");
        } else if (txtSoDT.getText().length() < 9 || txtSoDT.getText().length() > 13) {
            DiaLogHelper.alert(this, "Số điện thoại phải nhập đủ 10 hoặc 11 số.");
        } else if (!txtSoDT.getText().matches("[0-9]+")) {

            DiaLogHelper.alert(this, "Số điện thoại chỉ nhập số");
         
        } else if (txtEmail.getText().equals("")) {
            DiaLogHelper.alert(this, "Email không đươc để trống");
           

//        } else if (txtNgaySinh.getText().equals("")) {
//            DiaLogHelper.alert(this, "Ngày sinh không đươc để trống");
//        }
        }else{
            flag=true;
        }
        
    }

    private void TimKiemKH() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        String TenNV = txtTimKiem.getText();
        List<KhachHang> list = daokh.selectByKeyWord(TenNV);
        for (KhachHang kh : list) {
            Object[] row = {kh.getMaKH(), kh.getTenKH(), kh.getSoDT(), kh.isGioiTinh() ? "Nam" : "Nữ", kh.getEmail(), kh.getDiaChi(), kh.getTrangThai()};
            model.addRow(row);

        }

    }

    private void TimKiemDC() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        String DiaChi = txtLocDiaChi.getText();
        List<KhachHang> list = daokh.selectByDiaChi(DiaChi);
        for (KhachHang kh : list) {
            Object[] row = {kh.getMaKH(), kh.getTenKH(), kh.getSoDT(), kh.isGioiTinh() ? "Nam" : "Nữ", kh.getEmail(), kh.getDiaChi(), kh.getTrangThai()};
            model.addRow(row);

        }

    }

