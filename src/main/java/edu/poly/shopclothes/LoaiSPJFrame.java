 private void insert() {
        DanhMucSP kh = getForm();

        try {
            dao.insert(kh);
            DiaLogHelper.alert(this, "Thêm Mới Loại SP Thành  Công");
            fillTable();
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }

    }

    private void update() {
        DanhMucSP kh = getForm();

        try {
            dao.update(kh);
            DiaLogHelper.alert(this, "Cập Nhât thành Công");
            fillTable();
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }

    }

    private void delete() {

             Integer manv = (Integer) tblDanhMuc.getValueAt(this.index, 0);

        try {
            dao.delete(manv);
            DiaLogHelper.alert(this, "Xóa Thanh công");
            fillTable();
            btnMoiActionPerformed(null);
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }

    }
       void Edit() {
        try {
            Integer manv = (Integer) tblDanhMuc.getValueAt(this.index, 0);
            DanhMucSP model = dao.selectById(manv);
            if (model != null) {
                
                this.setForm(model);
                
            }
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }
    }

