    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblDanhMuc.getModel();
        model.setRowCount(0);
        List<DanhMucSP> list = dao.selectAll();
        for (DanhMucSP dm : list) {
            Object row[] = {dm.getID_DanhMuc(), dm.getTenDM()};
            model.addRow(row);
        }

    }

